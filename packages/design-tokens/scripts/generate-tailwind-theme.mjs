#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '../../..');

function read(file) {
  return readFileSync(resolve(here, '..', 'src', file), 'utf8');
}

function extractObject(source, name) {
  const match = source.match(new RegExp(`export const ${name} = (\\{[\\s\\S]*?\\}) as const;`));
  if (!match) throw new Error(`Could not find ${name} in source`);
  return new Function(`return ${match[1]}`)();
}

const colorsSrc = read('colors.ts');
const typographySrc = read('typography.ts');
const spacingSrc = read('spacing.ts');

const colors = extractObject(colorsSrc, 'colors');
const fontFamilies = extractObject(typographySrc, 'fontFamilies');
const spacing = extractObject(spacingSrc, 'spacing');

function flattenColors(obj, prefix = '') {
  const out = {};
  for (const [key, value] of Object.entries(obj)) {
    const kebab = (prefix ? `${prefix}-` : '') + key.replace(/[A-Z]/g, m => `-${m.toLowerCase()}`);
    if (typeof value === 'string') {
      out[kebab] = value;
    } else {
      Object.assign(out, flattenColors(value, kebab));
    }
  }
  return out;
}

const flatColors = flattenColors(colors);

const lines = [
  '/* Generated by packages/design-tokens/scripts/generate-tailwind-theme.mjs. Do not edit by hand. */',
  '',
  '@theme {',
];

for (const [name, hex] of Object.entries(flatColors)) {
  lines.push(`  --color-${name}: ${hex};`);
}
lines.push('');

lines.push(`  --font-display: var(--font-display-loaded), 'Caveat', cursive;`);
lines.push(`  --font-block: var(--font-block-loaded), 'Oswald', sans-serif;`);
lines.push(`  --font-pencil: var(--font-pencil-loaded), 'Architects Daughter', cursive;`);
lines.push(`  --font-pencil-mono: var(--font-pencil-mono-loaded), 'Special Elite', monospace;`);
lines.push('');

for (const [step, px] of Object.entries(spacing)) {
  lines.push(`  --spacing-${step}: ${px}px;`);
}

lines.push('}');
lines.push('');

const outPath = resolve(repoRoot, 'apps/admin/src/app/tokens.css');
writeFileSync(outPath, lines.join('\n'));
console.log(`Wrote ${outPath} (${Object.keys(flatColors).length} colors, ${Object.keys(fontFamilies).length} fonts, ${Object.keys(spacing).length} spacing steps)`);
